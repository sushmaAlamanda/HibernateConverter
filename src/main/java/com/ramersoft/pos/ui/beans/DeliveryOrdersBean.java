package com.ramersoft.pos.ui.beans;

import java.util.Date;

public class DeliveryOrdersBean {
	
	private Date Delivery_datetime;
	private long BillNo;
	private String Customer_name;
	private String Phonenumber;
	
	public Date getDelivery_datetime() {
		return Delivery_datetime;
	}
	
	public void setDelivery_datetime(Date delivery_datetime) {
		Delivery_datetime = delivery_datetime;
	}
	
	public long getBillNo() {
		return BillNo;
	}
	
	public void setBillNo(long billNo) {
		BillNo = billNo;
	}
	
	public String getCustomer_name() {
		return Customer_name;
	}
	public void setCustomer_name(String customer_name) {
		Customer_name = customer_name;
	}
	
	public String getPhonenumber() {
		return Phonenumber;
	}
	
	public void setPhonenumber(String phonenumber) {
		Phonenumber = phonenumber;
	}

	
	@Override
	public String toString() {
		return "DeliveryOrdersBean [Delivery_datetime=" + Delivery_datetime + ", BillNo=" + BillNo + ", Customer_name="
				+ Customer_name + ", Phonenumber=" + Phonenumber + "]";
	}
	
	
	

}
